// Toán tử liên quan tới số học trong C#

float a = 5;
int b = 4;

Console.WriteLine("Tổng của a + b = {0}", a + b);
Console.WriteLine("Hiệu của a - b = {0}", a - b);
Console.WriteLine("Tích của a * b = {0}", a * b);
Console.WriteLine("Thương của a / b = {0}", a / b);
Console.WriteLine("Số dư của a % b = {0}", a % b);

// Toán tử gán: =, +=, -=, *=, /=, %=

// Toán tử tăng giảm: ++, --
// ++ đằng sau thì là tăng sau khi sử dụng giá trị hiện tại, còn ++ đằng trước thì là tăng trước khi sử dụng giá trị hiện tại